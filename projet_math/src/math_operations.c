#include "math_operations.h"

/**
 * @brief Function to perform addition
 * 
 * This function takes two integers and returns their sum.
 *
 * @param a The first integer
 * @param b The second integer
 * @return The sum of a and b
 */
int add(int a, int b) {
    return a + b;
}

/**
 * @brief Function to perform subtraction
 * 
 * This function takes two integers and returns their difference.
 *
 * @param a The first integer
 * @param b The second integer
 * @return The difference between a and b
 */
int subtract(int a, int b) {
    return a - b;
}

/**
 * @brief Function to perform multiplication
 * 
 * This function takes two integers and returns their product.
 *
 * @param a The first integer
 * @param b The second integer
 * @return The product of a and b
 */
int multiply(int a, int b) {
    return a * b;
}
